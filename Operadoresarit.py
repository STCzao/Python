#OPERADORES ARITMETICOS 

#Los operadores aritmeticos, son aquellos que "manipulan" datos numericos, tanto numeros enteros, asi como numeros decimales tambien conocidos como reales
#Estos operadores son los mas sencillos de todos, se utilizan para realizar operaciones aritmeticas basicas, como son, sumas, restas, multiplicaciones, divisiones, modulos o residuo y exponenciales
#Al igual que todos los lenguajes de programacion, Python tambien cuenta con los siguientes operadores aritmeticos: 

#Suma (+) Resta (-) Multiplicacion (*) Exponente (**) Modulo (%) Division (/) Division entera(//)
 
#La suma o adicion, es la reunion de dos o mas conjuntos llamados "sumandos" en un solo conjunto llamado "suma o total". Su signo en Python es (+) y se coloca entre los sumandos

print("Suma:")

numero_uno = 5
numero_dos = 4

resultado = numero_uno + numero_dos

print("El resultado de la suma es: " + str(resultado))

#La resta, es la operacion contraria a la suma, y tambien recibe el nombre de sustraccion. La cual consiste en extraer o quitar de un numero mayor o menor. Su signo en Python es (-)

print("Resta:")

numero_uno = 5
numero_dos = 4

resultado = numero_uno - numero_dos

print("El resultado de la resta es: " + str(resultado))

#La multiplicacion, es la operacion matematica que consiste en hallar el resultado de sumar un numero tantas veces como indique otro. Su signo en Python es (*)

print("Multiplicacion:")

numero_uno = 5
numero_dos = 4

resultado = numero_uno * numero_dos

print("El resultado de la multiplicacion es: " + str(resultado))

#La potenciacion, es la operacion matematica mediante el cual multiplicamos un numero por si mismo las veces que nos indique el exponente. Su presentacion en Python es (**)

print("Exponente:")

numero_uno = 5
exponente = 5

resultado = numero_uno ** exponente

print("El resultado del exponente es: " + str(resultado))

#La division, es una operacion matematicas que consiste en averiguar cuantas veces un numero esta contenido en otro numero. Su signo en Python es (/)

print("Division:")

numero_uno = 5
numero_dos = 2

resultado = numero_uno / numero_dos 

print("El resultado de la division es: " + str(resultado))

#El modulo o resto, es la cantidad que sobra despues de efectuar una division, es decir, es el valor que se obtiene cuando un numero no puede ser dividido exactamente por otro. Su signo en Python es (%)

print("Modulo:")

numero_uno = 30
numero_dos = 8

resultado = numero_uno % numero_dos 

print("El resultado del modulo es: " + str(resultado))

#La division entera, es una operacion que consiste en obtener el valor entero de un numero, el cual resulte de una division entre dos numeros decimales o reales. Su representacion es Python es (//)

print("Division entera:")

numero_uno = 10
numero_dos = 3

resultado = numero_uno // numero_dos 

print("El resultado de la division entera es: " + str(resultado))