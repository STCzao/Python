#PALABRAS RESERVADAS

#Las palabras reservadas, son Identificadores para uso exclusivo del lenguaje de programacion, que no pueden ser utilizadas para identificar y nombrar variables, metodos, objetos o cualquier elemento dentro de nuestro codigo
#Al intentar utilizar alguna de estas palabras reservadas fuera del uso que tienen asignado, lo que obtendremos sera un error de compilacion y nuestro programa no podra ser ejecutado
#En python existe un total de 28 palabras reservadas, las cuales fueron asignadas para usos exclusivos del lenguaje de programacion

#Estas palabras son las siguientes: 

# False, None, True, and, as, assert, async, await, break, class, continue, def,
# del, elif, else, except, finally, for, from, global, if, import, in, is,
# lambda, nonlocal, not, or, pass, raise, return, try, while, with, yield

